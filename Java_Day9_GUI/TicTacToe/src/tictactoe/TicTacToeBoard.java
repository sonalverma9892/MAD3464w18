    public void select(JButton btn, int cell){
        int row = cell / 10;
        int col = cell % 10;
        
        if(player){
            board[row][col] = 'X';
            btn.setIcon(X);
        }else{
            board[row][col] = 'O';
            btn.setIcon(O);
        }
        
        player = !player;
        btn.setEnabled(false);
        checkWin();
    }
    
    public void checkWin(){
        
        for (int i=0; i<3; i++){
            if ((board[i][0] == board [i][1]) && (board[i][0] == board [i][2] && (board[i][0] != 'S'))) {
                JOptionPane.showMessageDialog(this, "Player " + board[i][0] + " Wins....!!!!");
                disableAllButtons();
                win = true;
            }
        }
        
        //check for  column win
        for (int i=0; i<3; i++){
            if ((board[0][i] == board [1][i]) && (board[0][i] == board [2][i] && (board[0][i] != 'S'))) {
                JOptionPane.showMessageDialog(this, "Player " + board[0][i] + " Wins....!!!!");
                disableAllButtons();
                win = true;
            }
        }
        
        //check for  diagonal win
        
            if (((board[0][0] == board [1][1]) && 
                    (board[0][0] == board [2][2]) && 
                    (board[0][0] != 'S')) || 
                    (((board[0][2] == board [1][1]) && 
                    (board[0][2] == board [2][0]) && 
                    (board[0][2] != 'S')))) {
                JOptionPane.showMessageDialog(this, "Player " + board[1][1] + " Wins....!!!!");
                disableAllButtons();
                win = true;
            }
        
        if(win){
            //ask to play again
             int answer = JOptionPane.showConfirmDialog(this, "Do you want to play again ?"," Play again", JOptionPane.YES_NO_OPTION);
            if (answer == JOptionPane.YES_OPTION){
               restartGame();
            }else{
                System.exit(0);
            }      
        }
    }
    
